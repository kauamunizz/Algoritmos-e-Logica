// Operadores Aritmeticos no VisualG

// Operador | Significado
//        + | adição
//        - | subtração
//        * | multiplicação
//        / | divisão
//        \ | divisão inteira
// % ou mod | resto da divisão (inteiro)
//        ^ | potenciação (real)

// Precedencia:
// 1° lugar: ^
// 2° lugar: * / \ %
// 3° lugar: + -

//Exemplos de expressoes aritmeticas:
2 * 6 / 3
//4

3 + 2 * 4
// 11

(3 + 2) * 4
// 20

2 * 3 ^ 4
//

60 / (3 + 2) * 4
//

60 / ((3 + 2) * 4)
